struct M6k8TestBus : public emu::M6800Bus<h_uint8_t, h_uint16_t>
{
    ByteType readByte(WordType addr) const override
    {
        if(!emu::isValidInt(addr))
            return {};
        auto address = asNativeInt(addr);
        if(address < ram.size())
            return ram[address];
        //if(address >= 0x8010 && address < 0x8020)
        //    return _impl->_pia.readByte(addr & 3);
        if(address >= 0xC000)
            return dream6800Rom[address & 0x3ff];
        return ByteType(0);
    }

    void writeByte(WordType addr, ByteType val) override
    {
        if(emu::isValidInt(addr) && addr < 4096)
            ram[asNativeInt(addr)] = val;
    }

    std::array<h_uint8_t,4096> ram;
};

